# Parse the tab-separated data
phone_map = {}
for line in raw_list.split("\n"):
    line = line.strip()
    if not line:
        continue
    parts = line.split("\t")
    if len(parts) >= 2:
        # Last element is the phone number
        phone = parts[-1].strip()
        # First element is the first name
        first_name = parts[0].strip().capitalize()
        if phone and first_name:
            # Clean number to digits
            clean_phone = "".join(filter(str.isdigit, phone))
            if len(clean_phone) >= 10:
                phone_map[clean_phone] = first_name

# Write to phone_names.json
with open("phone_names.json", "w") as f:
    json.dump(phone_map, f, indent=2)

print(f"✅ Created phone_names.json with {len(phone_map)} mapped records.")

